package com.example.activitiescomrecycler;

public class Users
{
    private String title;
    private String body;

    public Users(String title, String body)
    {
        this.title = title;
        this.body = body;
    }

    public String getTitle()
    {
        return title;
    }

    public String getBody()
    {
        return body;
    }
}
